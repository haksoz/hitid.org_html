<h4>GUIDELINES FOR ABSTRACT SUBMISSION</h4>

<p>1. All abstracts must be written in ENGLISH.</p>

<p>2. The title of the abstract should be written in CAPITAL LETTERS (bolt), and limited to 20 words revealing its content. No abbreviations should be used in the title. </p>

<p>3. Include the author and co-authors' details <br>
• Full first and family name(s) <br>
• Affiliation details: department, institution/hospital, city, country <br>
• The name of the presenting author should be underlined </p>

<p>4. Abstracts should not exceed 250 words, font Ariel, size 10, line spacing 1.5. </p>

<p>5. The abstract should contain the following information (without the headings): Objectives, Methods, Results, Conclusion </p>

<p>6. Use only standard abbreviations. Place special or unusual abbreviations in parentheses after first use. </p>

<p>7. Tables and Figures will not be accepted. </p>

<p>8. At the end of the abstract <br>
• include presenting author's contact details: email address <br>
• note the presentation type preference (oral presentation/poster presentation) </p>

<p>9. Abstracts must be sent in a Microsoft Word file (.doc or .docx) to hitid@soloevent.net before September 30, 2024. </p>

<p>10. After submission, you will receive a confirmation to the e-mail from which it was sent. It is recommended that this email address belongs to the corresponding/presenting author. If you do not receive a confirmation email within 2 days, please contact us at <a href="mailto:bept2023sofia@gmail.com">bept2023sofia@gmail.com</a></p>

<p>11. Notifications of the approved abstracts will be sent to the email address provided by the presenting author no later than October 15, 2023. </p>

<p>12. No changes to the submitted abstracts will be made by the organizers. If you have any questions, please contact us at <a href="mailto:hitid@soloevent.net">hitid@soloevent.net</a></p>

<p>13. All abstracts accepted for presentation will be published on the website of the Bulgarian Association of Clinical Immunology (BACI) before the conference. </p>

<p>14. All abstracts accepted for presentation will be published in the Bulgarian Journal of Clinical Immunology</p>


