                                        19.13) Dernek Tüzüğü ile yönetmeliklerinin hazırlanması ya da değiştirilmesine ilişkin çalışmalar yapıp, gereken tasarıları Genel Kurulun onayına sunmak.
                                    </p>
                                    <p class="mb-0">
                                        19.14) Çalışma alanları ile ilgili yönetmelikler hazırlayarak genel kurula sunmak. İlgili yönetmelikler gereğince yapılması gereken işlemleri yürütmek. (Kongre, toplantı, çalıştaylar, burslar v.b.)
                                    </p>
                                    <p class="mb-0">
                                        <strong>İç Denetim</strong>
                                    </p>
                                    <p class="mb-0">
                                        <strong>MADDE 20 –</strong> Dernek her türlü denetime açık olduğu gibi Genel Kurul, Yönetim Kurulu ve Denetim Kurulu, yapılan ya da yapılamayan çalışmaları ve uygulamaları, iç kontrol sisteminin bütünlüğünü göz önünde bulundurarak işlevlerin etkinliği ve verimliliği, mali raporlama sisteminin güvenirliliği, yasa ve düzenlemelere uygunluk açısından inceler, değerlendirir ve denetim sonuçları yetkisine giren organlar tarafından gereken kararlar alınmak suretiyle iç denetimin gerekleri yerine getirir.
                                    </p>
                                    <p class="mb-0">
                                        Gerekirse bağımsız denetim kuruluşlarına iç denetim yaptırılabilir.
                                    </p>
                                    <p class="mb-0">
                                        <strong>Denetim Kurulunun görev ve yetkileri</strong>
                                    </p>
                                    <p class="mb-0">
                                        <strong>MADDE 21 –</strong> Derneğin yetkili denetim organı olan Denetim Kurulu, Genel Kurulda gizli oyla 3 (üç) yıl için seçilen üç asıl ve üç yedek üyeden oluşur.
                                    </p>
                                    <p class="mb-0">
                                        Denetim Kurulunun görev ve yetkileri aşağıda belirtilmiştir.
                                    </p>
                                    <p class="mb-0">
                                        21.1) Derneğin Tüzüğünde gösterilen amaç ve amacın gerçekleştirilmesi için sürdürüleceği belirtilen çalışma konuları doğrultusunda faaliyet gösterip göstermediğini araştırmak ve incelemek,
                                    </p>
                                    <p class="mb-0">
                                        21.2) Derneğin bütçe, hesap ve işlemleri ile ilgili defter ve belgelerinin yasalar ile Dernek Tüzüğüne uygun olarak tutulup tutulmadığını araştırmak ve incelemek,
                                    </p>
                                    <p class="mb-0">
                                        Denetim Kurulu yukarıda belirtilen inceleme ve araştırmalarını, 1 (bir) yılı geçmeyen aralıklarla toplanarak yapar ve denetim sonuçlarını birer raporla Yönetim Kurulu ile toplandığında Genel Kurula sunar.
                                    </p>
                                    <p class="mb-0">
                                        Etik Kurul Görev ve Yetkileri
                                    </p>
                                    <p class="mb-0">
                                        <strong>MADDE 22 –</strong> Etik Kurul, “HLA immünogenetiği, doku tipleme ve transplantasyon immünolojisi” alanında hekimlik uygulaması veya meslekle ilgili bir görevde bulunan; bu alanda en az 10 (on) yıldır kesintisiz çalışan; ödenti borcu olmayan ve Dernek yetkili kurullarınca haklarında herhangi bir cezai işlem ve yaptırım uygulanmamış üyeler arasından Genel Kurulca gizli oyla 3(üç) yıl için seçilen 3 (üç) asıl ve 3 (üç) yedek üyeden oluşur. Etik Kurul, seçilmesinden sonraki bir hafta içinde düzenleyeceği ilk toplantısında bir Başkan ve bir Sekreter seçer. Yılda bir kez olağan olarak toplanır. Gerektiğinde Yönetim Kurulu tarafından olağanüstü toplantıya çağrılabilir.
                                    </p>
                                    <p class="mb-0">
                                        Etik Kurul toplantı yeter sayısı 3 (üç) olup kararlar salt çoğunlukla alınır. Etik Kurul koşullar elverdiğince hukuk danışmanı ile birlikte toplanır. Dernek Başkanı veya Yönetim Kurulu’nun kendi içinden görevlendirdiği bir üye, oy hakkı olmaksızın bu toplantılara katılarak bilgi alışverişinde bulunabilir.
                                    </p>
                                    <p class="mb-0">
                                        Etik Kurul’un görev ve yetkileri aşağıda belirtilmiştir.
                                    </p>
                                    <p class="mb-0">
                                        22.1) “HLA immünogenetiği, doku tipleme ve transplantasyon immünolojisi” alanında etik farkındalık yaratmak üzere eğitimler yapmak, eğitim gereçleri hazırlamak, meslek onurunun korunması için çalışmalar yapmak, bu konuda her türlü önlemi almak ve öneriler getirmek,
                                    </p>
                                    <p class="mb-0">
                                        22.2) Yönetim Kurulunca iletilen şikayetleri ve dosyaları, Dernek Tüzüğü ve ilgili yasa, yönetmelik ve yönergeler çerçevesinde usul ve esas bakımından değerlendirmek, dosya içeriği hakkında rapor yazmak ve yaptırım önerilerinde bulunmak,
                                    </p>
                                    <p class="mb-0">
                                        22.3) Yönetim Kurulunca gönderilen dosyalar üzerinde verilen süre içinde yetişecek şekilde çalışmalar yapmak ve konuyu karara bağlayarak kurul kararını içeren raporu ve dosyayı tüm içeriğiyle birlikte Yönetim Kurulu’na iade etmek,
                                    </p>
                                    <p class="mb-0">
                                        22.4) Gerekli hallerde belli konularda ek araştırma ve soruşturma komisyonları kurulması için Yönetim Kuruluna öneri sunmak.
                                    </p>
                                    <p class="mb-0">
                                        <strong>Alt Çalışma Grupları Çalışma Esasları</strong>
                                    </p>
                                    <p class="mb-0">
                                        <strong>MADDE 23 -</strong> Yönetim Kurulu ilgili konularda alt çalışma gruplarını ve başkanlarını (doku tipleme laboratuvarı sorumlusu ya da sorumlu yardımcısı olarak görev yapan dernek üyeleri arasından) belirler ve Genel Kurul toplantılarında alt grup çalışmalarının sunulmasını, aktivitelerin sürekliliğini sağlar.
                                    </p>
                                    <p class="mb-0">
                                        <strong>Kurullardan Ayrılmaya İlişkin Hükümler</strong>
                                    </p>
                                    <p class="mb-0">
                                        <strong>MADDE 24 –</strong> Yönetim, Denetim ve Etik Kurul üyeleri, mazeretsiz olarak üst üste 3 (üç) toplantıya ya da mazeretli olsa bile görev süresinin bir yılı içinde yapılan toplantıların yarısından bir fazlasına katılmadıkları takdirde kurullardan ayrılmış sayılırlar.
                                    </p>
                                    <p class="mb-0">
                                        Herhangi bir nedenle görev aldığı kuruldan ayrılan üyenin yerine ilk sıradaki yedek üye, bir hafta içinde asıl üyeliğe çağrılır.
                                    </p>
                                    <p class="mb-0">
                                        <strong>IV. BÖLÜM TEMSİLCİLİKLER VE PLATFORM OLUŞTURMA</strong>
                                    </p>
                                    <p class="mb-0">
                                        <strong>Temsilcilikler</strong>
                                    </p>
                                    <p class="mb-0">
                                        <strong>MADDE 25 –</strong> Dernek, gerekli görülen yerlerde dernek faaliyetlerini yürütmek amacıyla Yönetim Kurulu kararı uyarınca temsilcilikler açabilir. Temsilciliklerin adresi ve faaliyet alanı ile görev ve yetkileri, Dernek Yönetim Kurulu kararıyla temsilci olarak görevlendirilen kişi ya da kişiler tarafından o yerin mülki idare amirliğine yazılı olarak bildirilir.
                                    </p>
                                    <p class="mb-0">
                                        <strong>Platform Oluşturma</strong>
                                    </p>
                                    <p class="mb-0">
                                        <strong>MADDE 26 – </strong>Dernek, amaçları ile ilgisi bulunan ve kanunlarla yasaklanmayan alanlarda, diğer dernekler ya da vakıf, sendika ve benzeri sivil toplum kuruluşlarıyla ortak bir amacı gerçekleştirmek üzere ve Yönetim Kurulunun kararı ile mutabık kalınacak protokol hükümleri uyarınca platformlar oluşturabilir.
                                    </p>
                                    <p class="mb-0">
                                        Platformlar, kanunların dernekler için yasakladığı amaç ve faaliyet konuları doğrultusunda kurulamazlar ve faaliyet gösteremezler.
                                    </p>
                                    <p class="mb-0">
                                        <strong>V. BÖLÜM MALİ HÜKÜMLER</strong>
                                    </p>
                                    <p class="mb-0">
                                        <strong>Hesap Dönemi ve Bütçe Uygulaması</strong>
                                    </p>
                                    <p class="mb-0">
                                        <strong>MADDE 27 –</strong> Derneğin hesap dönemi bir takvim yılıdır. Hesap dönemi 1 Ocak’ta başlar ve 31 Aralık’ta sona erer.
                                    </p>
                                    <p class="mb-0">
                                        Derneğin bütçesi, iki hesap dönemi için ayrı ayrı yapılır ve gelir-gider çizelgelerinden oluşur.
                                    </p>
                                    <p class="mb-0">
                                        <strong>Derneğin Gelir Kaynakları</strong>
                                    </p>
                                    <p class="mb-0">
                                        <strong>MADDE 28 – </strong>Derneğin gelir kaynakları aşağıda belirtilmiştir.
                                    </p>
                                    <p class="mb-0">
                                        28.1) Üye aidatları; Dernek asıl üyelerin ödeyeceği yıllık aidat miktarı Yönetim Kurulunca belirlenir.
                                    </p>
                                    <p class="mb-0">
                                        28.2) Dernekçe yapılan yayınlar, tertiplenen kongre, sempozyum, seminer, konferans, kurs, piyango, balo, temsil, konser, spor yarışması gibi faaliyetlerden sağlanacak gelirler.
                                    </p>
                                    <p class="mb-0">
                                        28.3) Derneğin para ve malvarlığından elde edilecek gelirler.
                                    </p>
                                    <p class="mb-0">
                                        28.4) İktisadi işletmeler ile ortaklıklardan sağlanacak gelirler.
                                    </p>
                                    <p class="mb-0">
                                        28.5) Yardım Toplama Kanunu hükümlerine uygun olarak toplanacak bağış ve yardımlar.
                                    </p>
                                    <p class="mb-0">
                                        <strong>Gelir Tablosu ve Bilanço Düzenlenmesi</strong>
                                    </p>
                                    <p class="mb-0">
                                        <strong>Madde 29 - </strong>İşletme hesabı esasına göre kayıt tutulması durumunda yılsonlarında (31 Aralık) (Dernekler Yönetmeliğinde belirtilen) ‘İşletme Hesabı Tablosu’ düzenlenir. Bilanço esasına göre defter tutulması durumunda ise, yılsonlarında (31 Aralık), Maliye Bakanlığınca yayımlanan Muhasebe Sistemi Uygulama Genel Tebliğlerini esas alarak bilanço ve gelir tablosu düzenlenir.
                                    </p>
                                    <p class="mb-0">
                                        <strong>Derneğin Gelir ve Gider İşlemleri</strong>
                                    </p>
                                    <p class="mb-0">
                                        <strong>Madde 30 -</strong> Gelir ve gider belgeleri
                                    </p>
                                    <p class="mb-0">
                                        30.1) Dernek gelirleri ‘alındı belgesi’ ile tahsil edilir.
                                    </p>
                                    <p class="mb-0">
                                        30.2) Dernek giderleri ise fatura, perakende satış fişi, serbest meslek makbuzu gibi harcama belgeleri ile yapılır. &nbsp;Ancak, 193 sayılı Gelir Vergisi Kanunu’nun 94 üncü maddesi kapsamında bulunan ödemeler için 213 sayılı Vergi Usul Kanunu hükümlerine göre gider pusulası düzenlenir. Bu kapsamda da bulunmayan ödemeler için Dernekler Yönetmeliği’nin eki Ek-13’te yer alan Gider Makbuzu veya banka dekontu gibi belgeler harcama belgesi olarak kullanılır.
                                    </p>
                                    <p class="mb-0">
                                        30.3) Dernek tarafından kişi, kurum veya kuruluşlara yapılacak bedelsiz mal ve hizmet teslimleri ‘Ayni Yardım Teslim Belgesi’ ile yapılır. Kişi, kurum veya kuruluşlar tarafından derneklere yapılacak bedelsiz mal ve hizmet teslimleri ise ‘Ayni Bağış Alındı Belgesi’ ile kabul edilir.
                                    </p>
                                    <p class="mb-0">
                                        30.4) Alındı Belgeleri
                                    </p>
                                    <p class="mb-0">
                                        Dernek gelirlerinin tahsilinde kullanılacak ‘Alındı Belgeleri’ yönetim kurulu kararıyla, matbaaya bastırılır. Ayni bağış alındı belgeleri Dernekler Yönetmeliği hükümlerine uygun olarak elektronik ortamda da düzenlenebilir.
                                    </p>
                                    <p class="mb-0">
                                        Alındı belgelerinin bastırılması ve kontrolü, elektronik ortamda düzenlenmesi, matbaadan teslim alınması, deftere kaydedilmesi, eski ve yeni saymanlar arasında devir teslimi ve alındı belgesi ile dernek adına gelir tahsil edecek kişi veya kişiler tarafından bu alındı belgelerinin kullanımına ve toplanılan gelirlerin teslimine ilişkin hususlarda Dernekler Yönetmeliğinin ilgili hükümlerine göre hareket edilir.
                                    </p>
                                    <p class="mb-0">
                                        Dernek gelirlerinin bankalar aracılığı ile toplanması halinde banka tarafından düzenlenen dekont ya da hesap özeti gibi belgeler alındı belgesi yerine geçer. Alındı belgeleri ve harcama belgelerinin saklama süresi beş yıldır.
                                    </p>
                                    <p class="mb-0">
                                        30.5) Yetki Belgesi
                                    </p>
                                    <p class="mb-0">
                                        Yönetim kurulu asıl üyeleri ile Dernekler Kanunu’nun 24 üncü maddesine göre temsilci olarak görevlendirilenler hariç, dernek adına gelir tahsil edecek kişi veya kişiler, yetki süresi de belirtilmek suretiyle, yönetim kurulu kararı ile tespit edilir. Gelir tahsil edecek kişilerin açık kimliği, imzası ve fotoğraflarını ihtiva eden ‘Yetki Belgesi’ dernek tarafından iki nüsha olarak düzenlenerek, dernek yönetim kurulu başkanınca onaylanır. Yönetim kurulu asıl üyeleri yetki belgesi olmadan gelir tahsil edebilir.
                                    </p>
                                    <p class="mb-0">
                                        Yetki belgelerinin süresi yönetim kurulu tarafından en çok bir yıl olarak belirlenir. Süresi biten yetki belgeleri birinci fıkraya göre yenilenir. Yetki belgesinin süresini bitmesi veya adına yetki belgesi düzenlenen kişinin görevinden ayrılması, ölümü, işine veya görevine son verilmesi gibi hallerde, verilmiş olan yetki belgelerinin dernek yönetim kuruluna bir hafta içinde teslimi zorunludur. Ayrıca, gelir toplama yetkisi yönetim kurulu kararı ile her zaman iptal edilebilir.
                                    </p>
                                    <p class="mb-0">
                                        30.6) Dernek tarafından tutulacak defter ve kayıtlar ile ilgili usul ve esaslar, Dernekler Yönetmeliği hükümleri uyarınca düzenlenir ve defterler mülki idare amirliğinde ya da noterde onaylattırılır.
                                    </p>
                                    <p class="mb-0">
                                        <strong>Yardım ve İş Birliği</strong>
                                    </p>
                                    <p class="mb-0">
                                        <strong>MADDE 31 –</strong> Dernek, Tüzüğünde Madde 5’te belirtilen amacı ve bu amaca yönelik çalışma konularını gerçekleştirmek üzere benzer amaçlı derneklerden, vakıflardan, sendikalardan ve meslek kuruluşlarından maddi yardım alabilir ve Genel Kurulca onaylanmak koşuluyla adı geçen kurumlara maddi yardımda bulunabilir.
                                    </p>
                                    <p class="mb-0">
                                        Dernek, 5072 sayılı Dernek ve Vakıfların Kamu Kurum ve Kuruluşları ile İlişkilerine Dair Kanun hükümleri saklı kalmak üzere, kamu kurum ve kuruluşları ile görev alanlarına giren konularda ortak projeler yürütebilir. Bu projelerde kamu ve diğer (kobi, vakıf) kurum ve kuruluşları proje maliyetlerinin en fazla yüzde ellisi oranında Derneğe ayni ya da nakdi katkı sağlayabilirler.
                                    </p>
                                    <p class="mb-0">
                                        <strong>Derneğin Borçlanma Biçimleri</strong>
                                    </p>
                                    <p class="mb-0">
                                        <strong>MADDE 32 – </strong>Dernek, Genel Kurulun onayı ve Yönetim Kurulunun kararı gereğince, amaç ve çalışma konularının gerçekleşmesine yönelik olmak ya da mali kaynak sağlama maksadıyla, Dernek malvarlığının %50’sini aşmamak ve gelirlerine göre ödeyebilmek koşuluyla borçlanabilir.
                                    </p>
                                    <p class="mb-0">
                                        <strong>VI. BÖLÜM GENEL HÜKÜMLER</strong>
                                    </p>
                                    <p class="mb-0">
                                        <strong>Taşınmaz Mal Edinme</strong>
                                    </p>
                                    <p class="mb-0">
                                        <strong>MADDE 33 –</strong> Dernek, Genel Kurullarının yetki vermesi üzerine Yönetim Kurulu kararıyla taşınmaz mal satın alabilir, taşınmaz malları üzerinde ayni hak tesis edebilir ya da taşınmaz mallarını satabilir. Yönetim Kurulu, edinilen taşınmazları, tapuya tescilinden itibaren bir ay içinde mülki idare amirliğine bildirmek zorundadır.
                                    </p>
                                    <p class="mb-0">
                                        <strong>İktisadi İşletmeler</strong>
                                    </p>
                                    <p class="mb-0">
                                        <strong>MADDE 34 –</strong> Derneğin amaç ve hizmet konularını gerçekleştirmek; uygulama, eğitim ve gelire yönelik olmak üzere Dernek iktisadi işletmeleri kurulabilir.
                                    </p>
                                    <p class="mb-0">
                                        Dernek Yönetim Kurulu tarafından kabul edilecek Dernek iktisadi işletmesi statüsünde Derneğin kuruluşuna ilişkin bilgiler, Derneğin amaç ve hizmet konuları ile vergi bağışıklığı konusu, işletmenin çalışma konuları, işletmenin yönetim biçimi ve temsili, işletme yönetiminin sorumluluğu ve ilkeleri, mali işlemler, işletmenin denetimi gibi ilişkili hükümler yer alır.
                                    </p>
                                    <p class="mb-0">
                                        Dernek iktisadi işletmeleri, Ticaret Sicili mevzuatı hükümleri uyarınca tescil edilirler ve tescil tarihinden başlamak üzere yürürlüğe girerler.
                                    </p>
                                    <p class="mb-0">
                                        <strong>Tüzük Değişikliği</strong>
                                    </p>
                                    <p class="mb-0">
                                        <strong>MADDE 35 – </strong>Dernek Tüzüğü, Genel Kurula katılma hakkı bulunan üyelerin üçte ikisinin katıldığı toplantıda ya da çoğunluğun sağlanamaması durumunda ikinci toplantıya katılan üyelerin, 2/3 çoğunluk oyu ile değiştirilebilir. Ancak ikinci toplantıya Yönetim ve Denetim Kurulu asıl üyelerinin en az iki katı kadar üyenin katılması zorunludur.
                                    </p>
                                    <p class="mb-0">
                                        Değişiklik önerileri en az (7) gün önceden Genel Kurul Üyelerine duyurulmadıkça Genel Kurulda görüşme yapılamaz.
                                    </p>
                                    <p class="mb-0">
                                        <strong>Derneğin Tasfiyesi</strong>
                                    </p>
                                    <p class="mb-0">
                                        <strong>MADDE 36 –</strong> Dernek, tasfiyenin karara bağlanması için çağrılmış olan Genel Kurulda, Genel Kurula katılma hakkına sahip üyelerin en az (2/3)’ünün hazır bulunacağı toplantıya katılan üyelerin (2/3) oy çoğunluğu ile tasfiye edilir.
                                    </p>
                                    <p class="mb-0">
                                        İlk toplantıda (2/3) çoğunluk hazır bulunamadığı takdirde ikinci kez toplanmak üzere Genel Kurul üyeleri toplantıya çağrılır. Ancak ikinci toplantıya Yönetim ve Denetim Kurulu asıl üyelerinin en az iki katı kadar üyenin katılması ve tasfiye kararının, hazır bulunan üyelerin (2/3) oy çoğunluğu ile alınması zorunludur.
                                    </p>
                                    <p class="mb-0">
                                        Derneğin tasfiyesi, Yönetim Kurulu tarafından 5 (beş) gün içinde yörenin mülki idare amirliğine bildirilir.
                                    </p>
                                    <p class="mb-0">
                                        Dağıtılma, Dağıtma Kurulu olarak Yönetim Kurulu tarafından yapılır ve dağıtma bilançosu gereği çıkacak malvarlığı benzer amaçlı bir derneğe ya da vakfa devredilir.
                                    </p>
                                    <p class="mb-0">
                                        <strong>Hüküm Eksikliği</strong>
                                    </p>
                                    <p class="mb-0">
                                        <strong>MADDE 37 –</strong> Dernek Tüzüğünde hüküm eksikliğinin bulunması halinde Türk Medeni Kanunu ve Dernekler Kanunu ile Dernekler Yönetmeliği hükümleri uygulanır.
                                    </p>
                                    <p class="mb-0">
                                        <strong>Yönetmelikler</strong>
                                    </p>
                                    <p class="mb-0">
                                        <strong>MADDE 38 –</strong> Dernek Tüzüğü hükümleri Yönetim Kurulunun hazırlayacağı ve gerek gördüğünde Genel Kurulun onaylayacağı yönetmelikler hükümleri ile birlikte uygulanır.
                                    </p>
                                    <p class="mb-0">
                                        Geçici Madde 18.1) Geçici Yönetim Kurulu
                                    </p>
                                    <p class="mb-0">
                                        İlk genel kurulda dernek organları oluşturulana kadar, derneği temsil edecek ve dernekle ilgili iş ve işlemleri yürütecek olan geçici yönetim kurulu üyeleri aşağıda belirtilmiştir.
